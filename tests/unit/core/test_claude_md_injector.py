    def test_workspace_root_not_existing_does_not_raise(self, tmp_path: Path) -> None:
        """Missing workspace_root is handled gracefully (no exception)."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        nonexistent = tmp_path / "does_not_exist"
        handler = _StubHandler("h", "## H\n\nContent.")
        injector = ClaudeMdInjector(workspace_root=nonexistent, handlers=[handler])
