"""TDD tests for ClaudeMdInjector."""

from pathlib import Path
from typing import Any

_OPEN_TAG = "<hooksdaemon>"
_CLOSE_TAG = "</hooksdaemon>"
_AUTO_COMMENT = "<!-- Auto-generated by hooks daemon"


class _StubHandler:
    """Minimal handler stub satisfying HasClaudeMd Protocol for injection tests."""

    def __init__(self, name: str, claude_md: str | None) -> None:
        self.name = name
        self._claude_md = claude_md

    def get_claude_md(self) -> str | None:
        return self._claude_md

    def get_acceptance_tests(self) -> list[Any]:
        return []


class TestClaudeMdInjectorCreateSection:
    """Tests for creating/updating <hooksdaemon> section."""

    def test_injects_section_when_claude_md_has_none(self, tmp_path: Path) -> None:
        """Creates <hooksdaemon> section in CLAUDE.md that has no such section."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text("# My Project\n\nSome content.\n")

        handler = _StubHandler("pipe_blocker", "## Pipe Blocker\n\nDo not pipe to tail.")
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=[handler])
        injector.inject()

        content = claude_md.read_text()
        assert _OPEN_TAG in content
        assert _CLOSE_TAG in content
        assert "Pipe Blocker" in content
        assert "# My Project" in content  # original content preserved

    def test_replaces_existing_section(self, tmp_path: Path) -> None:
        """Overwrites existing <hooksdaemon> section with fresh content."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        old_content = (
            "# Project\n\n" f"{_OPEN_TAG}\nOLD CONTENT HERE\n{_CLOSE_TAG}\n\nOther stuff.\n"
        )
        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text(old_content)

        handler = _StubHandler("stop_handler", "## Stop\n\nNew guidance.")
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=[handler])
        injector.inject()

        content = claude_md.read_text()
        assert "OLD CONTENT HERE" not in content
        assert "New guidance" in content
        assert "Other stuff" in content  # content outside section preserved

    def test_creates_claude_md_when_missing(self, tmp_path: Path) -> None:
        """Creates CLAUDE.md with <hooksdaemon> section if file does not exist."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        handler = _StubHandler("my_handler", "## My Handler\n\nGuidance here.")
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=[handler])
        injector.inject()

        claude_md = tmp_path / "CLAUDE.md"
        assert claude_md.exists()
        content = claude_md.read_text()
        assert _OPEN_TAG in content
        assert "Guidance here" in content

    def test_skips_handlers_with_none_claude_md(self, tmp_path: Path) -> None:
        """Handlers returning None from get_claude_md() are excluded."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text("# Project\n")

        handlers: list[_StubHandler] = [
            _StubHandler("included", "## Included\n\nContent."),
            _StubHandler("excluded", None),
        ]
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=handlers)
        injector.inject()

        content = claude_md.read_text()
        assert "## Included" in content
        assert "excluded" not in content

    def test_noop_when_no_handlers_have_claude_md(self, tmp_path: Path) -> None:
        """Does not create/modify CLAUDE.md when all handlers return None."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        original = "# Project\n\nOriginal content.\n"
        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text(original)

        handlers: list[_StubHandler] = [
            _StubHandler("h1", None),
            _StubHandler("h2", None),
        ]
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=handlers)
        injector.inject()

        assert claude_md.read_text() == original

    def test_auto_comment_present_in_generated_section(self, tmp_path: Path) -> None:
        """Generated section includes auto-generated comment."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        claude_md = tmp_path / "CLAUDE.md"
