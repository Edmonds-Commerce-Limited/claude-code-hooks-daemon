    def test_oserror_during_inject_does_not_raise(self, tmp_path: Path) -> None:
        """OSError from file operations is caught and swallowed — never raises."""
        from unittest.mock import patch

        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        claude_md = tmp_path / "CLAUDE.md"
