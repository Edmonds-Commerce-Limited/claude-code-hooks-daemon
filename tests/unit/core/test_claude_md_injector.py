    def test_handler_without_get_claude_md_is_skipped(self, tmp_path: Path) -> None:
        """Objects without get_claude_md() are silently skipped."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        original = "# Project\n\nOriginal.\n"
        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text(original)

        class _NoProtocol:
            name = "no_protocol"
            # No get_claude_md() method

        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=[_NoProtocol()])
        injector.inject()

        assert claude_md.read_text() == original  # unchanged — no handlers contributed

    def test_inject_is_idempotent(self, tmp_path: Path) -> None:
        """Calling inject() twice produces same result as calling once."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        claude_md = tmp_path / "CLAUDE.md"
        claude_md.write_text("# Project\n")

        handler = _StubHandler("h", "## H\n\nContent.")
        injector = ClaudeMdInjector(workspace_root=tmp_path, handlers=[handler])
        injector.inject()
        first = claude_md.read_text()

        injector.inject()
        second = claude_md.read_text()

        assert first == second
