    def test_empty_handler_list_is_noop(self, tmp_path: Path) -> None:
        """Empty handler list does not modify CLAUDE.md."""
        from claude_code_hooks_daemon.core.claude_md_injector import ClaudeMdInjector

        original = "# Project\n\nOriginal.\n"
        claude_md = tmp_path / "CLAUDE.md"
