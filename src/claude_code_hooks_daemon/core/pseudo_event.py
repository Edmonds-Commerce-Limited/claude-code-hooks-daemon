"""Pseudo-event infrastructure for synthetic event dispatch.

A pseudo-event is a synthetic event generated by the daemon, triggered by real
events but with its own handler chain, dispatch, and configuration. Handlers
register FOR pseudo-events the same way they register for PreToolUse or Stop.

Key concepts:
- Multi-event triggers: A pseudo-event hooks into real events via notation
  like "pre_tool_use:1/5" (fire once every 5 PreToolUse events)
- Frequency control: Notation event_type:N/D means "fire N times every D
  occurrences"
- Setup functions: Shared preparation (e.g., transcript reading) runs ONCE
  per pseudo-event fire, then all handlers receive the prepared data
- Result merging: DENY wins, context accumulates
"""

from __future__ import annotations

import logging
from collections.abc import Callable
from dataclasses import dataclass
from typing import Any

from claude_code_hooks_daemon.core.chain import ChainExecutionResult, HandlerChain
from claude_code_hooks_daemon.core.event import EventType
from claude_code_hooks_daemon.core.hook_result import Decision, HookResult

logger = logging.getLogger(__name__)

# Type alias for setup functions: (hook_input, session_id) -> enriched_hook_input | None
SetupFunction = Callable[[dict[str, Any], str], dict[str, Any] | None]


@dataclass(frozen=True, slots=True)
class PseudoEventTrigger:
    """A trigger binding a pseudo-event to a real event type with frequency control.

    Notation: "event_type:N/D" — fire N times every D occurrences.
    Example: "pre_tool_use:1/5" fires once every 5 PreToolUse events.

    Attributes:
        event_type: The real event type that triggers this pseudo-event.
        numerator: How many times to fire per window.
        denominator: Window size (number of real events).
    """

    event_type: EventType
    numerator: int
    denominator: int

    @classmethod
    def from_string(cls, notation: str) -> PseudoEventTrigger:
        """Parse trigger notation string.

        Args:
            notation: String in format "event_type:N/D"

        Returns:
            Parsed PseudoEventTrigger

        Raises:
            ValueError: If notation format is invalid
        """
        if ":" not in notation:
            raise ValueError(f"Invalid trigger format: {notation!r}. Expected 'event_type:N/D'")

        event_part, freq_part = notation.split(":", 1)

        if "/" not in freq_part:
            raise ValueError(f"Invalid trigger format: {notation!r}. Expected 'event_type:N/D'")

        num_str, den_str = freq_part.split("/", 1)

        try:
            numerator = int(num_str)
            denominator = int(den_str)
        except ValueError:
            raise ValueError(
                f"Invalid trigger format: {notation!r}. N and D must be integers"
            ) from None

        if denominator <= 0:
            raise ValueError(f"Invalid trigger: denominator must be > 0, got {denominator}")

        if numerator <= 0:
            raise ValueError(f"Invalid trigger: numerator must be > 0, got {numerator}")

        if numerator > denominator:
            raise ValueError(
                f"Invalid trigger: numerator ({numerator}) must be <= denominator ({denominator})"
            )

        event_type = EventType.from_string(event_part)

        return cls(event_type=event_type, numerator=numerator, denominator=denominator)


@dataclass(frozen=True, slots=True)
class PseudoEventConfig:
    """Configuration for a pseudo-event.

    Attributes:
        name: Pseudo-event name (e.g., "nitpick").
        enabled: Whether this pseudo-event is active.
        triggers: List of trigger bindings to real events.
        handler_configs: Handler-specific config dict keyed by handler name.
    """

    name: str
    enabled: bool
    triggers: tuple[PseudoEventTrigger, ...]
    handler_configs: dict[str, dict[str, Any]]

    @classmethod
    def from_dict(cls, name: str, data: dict[str, Any]) -> PseudoEventConfig:
        """Parse pseudo-event config from YAML dict.

        Args:
            name: Pseudo-event name
            data: Config dict with keys: enabled, triggers, handlers

        Returns:
            Parsed PseudoEventConfig

        Raises:
            ValueError: If required fields are missing or invalid
        """
        enabled = data.get("enabled", True)

        trigger_strs = data.get("triggers")
        if not trigger_strs:
            raise ValueError(f"Pseudo-event {name!r} must have at least one trigger")

        triggers = tuple(PseudoEventTrigger.from_string(s) for s in trigger_strs)
        handler_configs = data.get("handlers", {})

        return cls(
            name=name,
            enabled=enabled,
            triggers=triggers,
            handler_configs=handler_configs,
        )


@dataclass(slots=True)
class _RegisteredPseudoEvent:
    """Internal: a registered pseudo-event with its setup function and handler chain."""

    config: PseudoEventConfig
    setup_fn: SetupFunction
    chain: HandlerChain


class PseudoEventDispatcher:
    """Manages pseudo-event triggering, setup, and handler chain dispatch.

    Maintains per-session counters for each pseudo-event trigger and orchestrates
    the setup → dispatch → result flow when a trigger fires.
    """

    __slots__ = ("_counters", "_registered")

    def __init__(self) -> None:
        """Initialise empty dispatcher."""
        self._registered: list[_RegisteredPseudoEvent] = []
        # Counters: {session_id: {pseudo_event_name: {event_type_value: count}}}
        self._counters: dict[str, dict[str, dict[str, int]]] = {}

    def register(
        self,
        config: PseudoEventConfig,
        *,
        setup_fn: SetupFunction,
        chain: HandlerChain,
    ) -> None:
        """Register a pseudo-event with its setup function and handler chain.

        Args:
            config: Pseudo-event configuration
            setup_fn: Function to prepare enriched hook_input
            chain: Handler chain for this pseudo-event
        """
        self._registered.append(
            _RegisteredPseudoEvent(config=config, setup_fn=setup_fn, chain=chain)
        )

    def check_and_fire(
        self,
        event_type: EventType,
        hook_input: dict[str, Any],
        session_id: str,
    ) -> list[HookResult]:
        """Check all pseudo-event triggers and fire those that match.

        Args:
            event_type: The real event type that just occurred
            hook_input: Original hook input from the real event
            session_id: Current session identifier

        Returns:
            List of HookResults from pseudo-event handler chains that fired
        """
        results: list[HookResult] = []

        for registered in self._registered:
            if not registered.config.enabled:
                continue

            for trigger in registered.config.triggers:
                if trigger.event_type != event_type:
                    continue

                if self._should_fire(registered.config.name, trigger, session_id):
                    result = self._fire(registered, hook_input, session_id)
                    if result is not None:
                        results.append(result)

        return results

    def _should_fire(
        self,
        pseudo_event_name: str,
        trigger: PseudoEventTrigger,
        session_id: str,
    ) -> bool:
        """Increment counter and check if pseudo-event should fire.

        Uses modular arithmetic: with N/D, fires when
        (count % D) falls in the last N positions of the window.
        This means for 1/5: fire on counts 5, 10, 15, ...
        For 2/6: fire on counts 3, 6, 9, 12, ... (positions 5 and 6 in window)

        Args:
            pseudo_event_name: Name of the pseudo-event
            trigger: The trigger being checked
            session_id: Current session

        Returns:
            True if the pseudo-event should fire
        """
        event_key = trigger.event_type.value

        # Initialise counter path
        if session_id not in self._counters:
            self._counters[session_id] = {}
        session_counters = self._counters[session_id]

        if pseudo_event_name not in session_counters:
            session_counters[pseudo_event_name] = {}
        event_counters = session_counters[pseudo_event_name]

        if event_key not in event_counters:
            event_counters[event_key] = 0

        # Increment
        event_counters[event_key] += 1
        count = event_counters[event_key]

        # Fire when count lands in the last N positions of each D-sized window
        # For 1/5: fires at 5, 10, 15 (remainder 0, which is in last 1 position)
        # For 2/6: fires at 3, 6, 9, 12 (remainder 3 and 0, last 2 positions mapped)
        remainder = count % trigger.denominator
        # Positions (1-indexed from end): denominator-N+1 ... denominator
        # In modular terms: remainder == 0 OR remainder > (denominator - numerator)
        if remainder == 0:
            return True
        return remainder > (trigger.denominator - trigger.numerator)

    def _fire(
        self,
        registered: _RegisteredPseudoEvent,
        hook_input: dict[str, Any],
        session_id: str,
    ) -> HookResult | None:
        """Run setup function and dispatch through handler chain.

        Args:
            registered: The registered pseudo-event
            hook_input: Original hook input
            session_id: Current session

        Returns:
            HookResult from handler chain, or None if setup returned None
        """
        try:
            enriched = registered.setup_fn(hook_input, session_id)
            if enriched is None:
                logger.debug(
                    "Pseudo-event %s setup returned None, skipping dispatch",
                    registered.config.name,
                )
                return None

            chain_result = registered.chain.execute(enriched)
            return chain_result.result

        except Exception:
            logger.exception(
                "Pseudo-event %s failed during setup/dispatch",
                registered.config.name,
            )
            return None


def merge_pseudo_results(
    real: ChainExecutionResult,
    pseudo_results: list[HookResult],
) -> ChainExecutionResult:
    """Merge pseudo-event results into the real chain result.

    Decision priority: DENY > ASK > ALLOW.
    Context always accumulates.

    Args:
        real: Result from the real handler chain
        pseudo_results: Results from pseudo-event handler chains

    Returns:
        The real ChainExecutionResult with merged decisions and context
    """
    if not pseudo_results:
        return real

    for pseudo in pseudo_results:
        # Accumulate context
        real.result.context.extend(pseudo.context)

        # Merge handlers
        for handler_name in pseudo.handlers_matched:
            if handler_name not in real.result.handlers_matched:
                real.result.handlers_matched.append(handler_name)

        # Decision priority: DENY > ASK > ALLOW
        if pseudo.decision == Decision.DENY:
            if real.result.decision != Decision.DENY:
                real.result.decision = Decision.DENY
                real.result.reason = pseudo.reason
        elif pseudo.decision == Decision.ASK:
            if real.result.decision == Decision.ALLOW:
                real.result.decision = Decision.ASK
                real.result.reason = pseudo.reason

    return real
