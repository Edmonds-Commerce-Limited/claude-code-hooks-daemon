"""ClaudeMdInjector — injects active handler guidance into project CLAUDE.md.

On daemon startup, the injector collects get_claude_md() from all active
handlers and writes a <hooksdaemon>...</hooksdaemon> section into the
project CLAUDE.md. This keeps agents informed about handler behaviour
without manual CLAUDE.md maintenance.

Usage:
    injector = ClaudeMdInjector(workspace_root=path, handlers=all_handlers)
    injector.inject()
"""

import logging
from pathlib import Path
from typing import Any, Protocol, runtime_checkable

logger = logging.getLogger(__name__)

# Section delimiters — must match exactly when reading/replacing
_OPEN_TAG = "<hooksdaemon>"
_CLOSE_TAG = "</hooksdaemon>"

# Auto-generated comment inside the section
_AUTO_COMMENT = (
    "<!-- Auto-generated by hooks daemon on restart. "
    "Do not edit this section — changes will be overwritten. -->"
)

# Section header shown inside the <hooksdaemon> block
_SECTION_HEADER = "## Hooks Daemon — Active Handler Guidance\n"

_SECTION_INTRO = (
    "The handlers listed below are active in this project. "
    "Read this section to avoid triggering unnecessary blocks.\n\n"
    "**When a tool is blocked by a handler, do not stop working.** "
    "Read the block reason, modify your approach, and continue with your task."
)


@runtime_checkable
class HasClaudeMd(Protocol):
    """Protocol for handlers that can provide CLAUDE.md guidance.

    Structural protocol — any class with name: str and get_claude_md()
    satisfies this interface without explicit inheritance.
    """

    name: str

    def get_claude_md(self) -> str | None:
        """Return markdown guidance, or None to exclude from injection."""
        ...


class ClaudeMdInjector:
    """Injects active handler guidance into project CLAUDE.md.

    Reads the project CLAUDE.md, finds (or creates) a
    <hooksdaemon>...</hooksdaemon> section, and replaces its content
    with fresh guidance collected from active handlers.

    Handlers that return None from get_claude_md() are excluded.
    If no handlers have guidance, CLAUDE.md is left untouched.
    All errors (missing file, permission errors) are logged and swallowed
    — injection is advisory and must never crash the daemon.

    Safety: before writing, the injector verifies that user content
    (everything outside the <hooksdaemon> block) is preserved. If user
    content would be lost, the write is aborted and the original file
    is left untouched. A .CLAUDE.md.pre-inject backup is always written
    before modifying an existing file.
    """

    # Backup filename — written before every modification to an existing CLAUDE.md
    _BACKUP_FILENAME = ".CLAUDE.md.pre-inject"

    def __init__(self, workspace_root: Path, handlers: list[Any]) -> None:
        """Initialise injector.

        Args:
            workspace_root: Project root containing CLAUDE.md
            handlers: All active handler instances
        """
        self._workspace_root = workspace_root
        self._handlers = handlers

    def inject(self) -> None:
        """Collect handler guidance and update CLAUDE.md.

        Silently no-ops if no handlers provide guidance.
        Logs errors but never raises — daemon startup must not fail.
        """
        try:
            self._run_inject()
        except OSError as exc:
            logger.warning(
                "ClaudeMdInjector: could not read/write %s — check file permissions (advisory skipped)",
                self._workspace_root / "CLAUDE.md",
                exc_info=exc,
            )
            return  # Advisory feature — daemon continues without CLAUDE.md injection
        except Exception as exc:  # nosec B110 - advisory, must never crash daemon
            logger.warning(
                "ClaudeMdInjector: unexpected error updating %s (advisory skipped)",
                self._workspace_root / "CLAUDE.md",
                exc_info=exc,
            )
            return  # Advisory feature — daemon continues without CLAUDE.md injection

    def _run_inject(self) -> None:
        """Execute injection — raises on errors for caller to handle."""
        sections = self._collect_sections()
        if not sections:
            logger.debug("ClaudeMdInjector: no handler guidance to inject — skipping")
            return

        generated = self._build_section(sections)
        claude_md_path = self._workspace_root / "CLAUDE.md"

        if not self._workspace_root.exists():
            logger.warning(
                "ClaudeMdInjector: workspace_root does not exist: %s", self._workspace_root
            )
            return

        if claude_md_path.exists():
            original = claude_md_path.read_text(encoding="utf-8")
            updated = self._replace_or_append_section(original, generated)

            # SAFETY: verify user content is preserved before writing
            if not self._is_user_content_preserved(original, updated):
                logger.error(
                    "ClaudeMdInjector: CONTENT LOSS DETECTED — aborting write to %s. "
                    "User content outside <hooksdaemon> block would be lost. "
                    "Original: %d chars, updated: %d chars. "
                    "Backup at: %s",
                    claude_md_path,
                    len(original),
                    len(updated),
                    claude_md_path.parent / self._BACKUP_FILENAME,
                )
                return

            # Write backup before modifying existing file
            backup_path = self._workspace_root / self._BACKUP_FILENAME
            backup_path.write_text(original, encoding="utf-8")

            claude_md_path.write_text(updated, encoding="utf-8")
        else:
            # No existing file — create from scratch (no backup needed)
            updated = generated + "\n"
            claude_md_path.write_text(updated, encoding="utf-8")

        logger.info(
            "ClaudeMdInjector: updated %s with guidance from %d handler(s)",
            claude_md_path,
            len(sections),
        )

    @staticmethod
    def _extract_user_content(content: str) -> str:
        """Return content outside the <hooksdaemon>...</hooksdaemon> block.

        If tags are not found, returns the full content (it is all user content).
        """
        open_pos = content.find(_OPEN_TAG)
        close_pos = content.find(_CLOSE_TAG)

        if open_pos != -1 and close_pos != -1 and open_pos < close_pos:
            before = content[:open_pos]
            after = content[close_pos + len(_CLOSE_TAG) :]
            return before + after

        # No valid tag pair — entire content is user content
        return content

    @staticmethod
    def _is_user_content_preserved(original: str, updated: str) -> bool:
        """Check that user content (outside hooksdaemon block) is preserved.

        Returns True if it is safe to write the updated content.
        Returns False if user content would be lost — the write must be aborted.
        """
        original_user = ClaudeMdInjector._extract_user_content(original)
        updated_user = ClaudeMdInjector._extract_user_content(updated)

        # Strip whitespace for comparison — whitespace changes are not content loss
        original_stripped = original_user.strip()
        updated_stripped = updated_user.strip()

        # If original had no user content, any result is safe
        if not original_stripped:
            return True

        # Updated must contain the same user content (stripped)
        return updated_stripped == original_stripped

    def _collect_sections(self) -> list[tuple[str, str]]:
        """Return list of (handler_name, guidance) for handlers with content."""
        result: list[tuple[str, str]] = []
        for handler in self._handlers:
            if not isinstance(handler, HasClaudeMd):
                continue
            content = handler.get_claude_md()
            if content is not None:
                result.append((handler.name, content))
        return result

    @staticmethod
    def _build_section(sections: list[tuple[str, str]]) -> str:
        """Build the full <hooksdaemon>...</hooksdaemon> block."""
        parts = [
            _OPEN_TAG,
            _AUTO_COMMENT,
            "",
            _SECTION_HEADER,
            _SECTION_INTRO,
            "",
        ]
        for _name, content in sections:
            parts.append(content.strip())
            parts.append("")
        parts.append(_CLOSE_TAG)
        return "\n".join(parts)

    @staticmethod
    def _replace_or_append_section(original: str, new_section: str) -> str:
        """Replace existing <hooksdaemon> block or append at end of file."""
        open_pos = original.find(_OPEN_TAG)
        close_pos = original.find(_CLOSE_TAG)

        if open_pos != -1 and close_pos != -1 and open_pos < close_pos:
            # Replace existing section (keep everything before and after)
            before = original[:open_pos]
            after = original[close_pos + len(_CLOSE_TAG) :]
            return before + new_section + after

        # Append at end (ensure single trailing newline before section)
        stripped = original.rstrip("\n")
        return stripped + "\n\n" + new_section + "\n"
